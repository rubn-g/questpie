describe("createAdminProjectionValidator", () => {
	const validator = createAdminProjectionValidator();

	it("returns no errors when both targets are absent", () => {
		const targets = new Map<string, CodegenResult>();
		expect(validator(targets)).toEqual([]);
	});

	it("returns no errors when only server target exists (no admin-client)", () => {
		const targets = new Map<string, CodegenResult>();
		targets.set("server", makeFakeResult("server", { blocks: ["hero"] }));
		expect(validator(targets)).toEqual([]);
	});

	it("returns no errors when only admin-client target exists (no server)", () => {
		const targets = new Map<string, CodegenResult>();
		targets.set(
			"admin-client",
			makeFakeResult("admin-client", { blocks: ["hero"] }),
		);
		expect(validator(targets)).toEqual([]);
	});

	it("returns no errors when server and client blocks match", () => {
		const targets = new Map<string, CodegenResult>();
		targets.set(
			"server",
			makeFakeResult("server", { blocks: ["hero", "cta"] }),
		);
		targets.set(
			"admin-client",
			makeFakeResult("admin-client", { blocks: ["hero", "cta"] }),
		);

		const errors = validator(targets);
		expect(errors).toEqual([]);
	});

	it("reports error for missing block renderer in admin-client", () => {
		const targets = new Map<string, CodegenResult>();
		targets.set(
			"server",
			makeFakeResult("server", { blocks: ["hero", "gallery"] }),
		);
		targets.set(
			"admin-client",
			makeFakeResult("admin-client", { blocks: ["hero"] }),
		);

		const errors = validator(targets);
		expect(errors.length).toBe(1);
		expect(errors[0].severity).toBe("error");
		expect(errors[0].category).toBe("blocks");
		expect(errors[0].key).toBe("gallery");
		expect(errors[0].sourceTarget).toBe("server");
		expect(errors[0].consumerTarget).toBe("admin-client");
		expect(errors[0].message).toContain("gallery");
		expect(errors[0].message).toContain("questpie/admin/blocks");
	});

	it("reports error for missing view definition in admin-client", () => {
		const targets = new Map<string, CodegenResult>();
		targets.set("server", makeFakeResult("server", { views: ["kanban"] }));
		targets.set("admin-client", makeFakeResult("admin-client", { views: [] }));

		const errors = validator(targets);
		expect(errors.length).toBe(1);
		expect(errors[0].category).toBe("views");
		expect(errors[0].key).toBe("kanban");
		expect(errors[0].message).toContain("questpie/admin/views");
	});

	it("reports error for missing component definition in admin-client", () => {
		const targets = new Map<string, CodegenResult>();
		targets.set("server", makeFakeResult("server", { components: ["rating"] }));
		targets.set(
			"admin-client",
			makeFakeResult("admin-client", { components: [] }),
		);

		const errors = validator(targets);
		expect(errors.length).toBe(1);
		expect(errors[0].category).toBe("components");
		expect(errors[0].key).toBe("rating");
		expect(errors[0].message).toContain("questpie/admin/components");
	});

	it("reports multiple errors across multiple categories", () => {
		const targets = new Map<string, CodegenResult>();
		targets.set(
			"server",
			makeFakeResult("server", {
				blocks: ["hero", "gallery"],
				views: ["kanban"],
				components: ["rating", "badge"],
			}),
		);
		targets.set(
			"admin-client",
			makeFakeResult("admin-client", {
				blocks: ["hero"],
				views: [],
				components: ["badge"],
			}),
		);

		const errors = validator(targets);
		// Missing: gallery (block), kanban (view), rating (component)
		expect(errors.length).toBe(3);

		const blockErr = errors.find(
			(e) => e.category === "blocks" && e.key === "gallery",
		);
		const viewErr = errors.find((e) => e.category === "views");
		const compErr = errors.find(
			(e) => e.category === "components" && e.key === "rating",
		);

		expect(blockErr).toBeDefined();
		expect(viewErr).toBeDefined();
		expect(viewErr!.key).toBe("kanban");
		expect(compErr).toBeDefined();
	});

	it("ignores categories not in PROJECTION_CHECKS", () => {
		const targets = new Map<string, CodegenResult>();
		// "widgets" is not checked by the admin projection validator
		targets.set("server", makeFakeResult("server", { widgets: ["stats"] }));
		targets.set(
			"admin-client",
			makeFakeResult("admin-client", { widgets: [] }),
		);

		const errors = validator(targets);
		expect(errors).toEqual([]);
	});

	it("returns no errors when server has empty categories", () => {
		const targets = new Map<string, CodegenResult>();
		targets.set(
			"server",
			makeFakeResult("server", { blocks: [], views: [], components: [] }),
		);
		targets.set("admin-client", makeFakeResult("admin-client", {}));

		const errors = validator(targets);
		expect(errors).toEqual([]);
	});

	it("handles missing category in client gracefully", () => {
		const targets = new Map<string, CodegenResult>();
		// Server has blocks, but admin-client has no blocks category at all
		targets.set("server", makeFakeResult("server", { blocks: ["hero"] }));
		targets.set(
			"admin-client",
			makeFakeResult("admin-client", {}), // no blocks category
		);

		const errors = validator(targets);
		expect(errors.length).toBe(1);
		expect(errors[0].category).toBe("blocks");
		expect(errors[0].key).toBe("hero");
	});

	it("includes suggestedDir in error messages", () => {
		const targets = new Map<string, CodegenResult>();
		targets.set(
			"server",
			makeFakeResult("server", {
				blocks: ["hero"],
				views: ["kanban"],
				components: ["rating"],
			}),
		);
		targets.set("admin-client", makeFakeResult("admin-client", {}));

		const errors = validator(targets);

		const blockErr = errors.find((e) => e.category === "blocks")!;
		const viewErr = errors.find((e) => e.category === "views")!;
		const compErr = errors.find((e) => e.category === "components")!;

		expect(blockErr.message).toContain("questpie/admin/blocks/hero.tsx");
		expect(viewErr.message).toContain("questpie/admin/views/kanban.tsx");
		expect(compErr.message).toContain("questpie/admin/components/rating.tsx");
	});
});
