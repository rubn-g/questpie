// Merge infrastructure
// ============================================================================

/**
 * Merge function: `(existing, incoming) → merged`.
 *
 * Plugins can provide custom merge functions for their contributed keys.
 * Common helpers are exported for reuse:
 * - {@link mergeRecord} — spread-merge two objects
 * - {@link mergeConcat} — concatenate two arrays
 * - {@link lastWins} — incoming replaces existing
 */
export type MergeFn = (existing: any, incoming: any) => any;

/** Spread-merge two records: `{ ...a, ...b }`. */
export const mergeRecord: MergeFn = (a, b) => ({
	...(a || {}),
	...(b || {}),
});

/** Concatenate two arrays: `[...a, ...b]`. */
export const mergeConcat: MergeFn = (a, b) => [...(a || []), ...(b || [])];

/** Merge two objects by concatenating any array-valued properties and merging the rest. */
export const mergeDeepConcat: MergeFn = (a, b) => {
	const result = { ...(a || {}) };
	for (const [key, value] of Object.entries(b || {})) {
		if (Array.isArray(result[key]) && Array.isArray(value)) {
			result[key] = [...result[key], ...value];
		} else if (result[key] !== undefined && typeof result[key] === "object" && typeof value === "object" && !Array.isArray(value)) {
			result[key] = { ...result[key], ...value };
		} else {
			result[key] = value;
		}
	}
	return result;
};

/** Last value wins (incoming replaces existing). */
export const lastWins: MergeFn = (_a, b) => b;

/** Deep-merge message dictionaries by locale. */
function mergeMessages(
	a: Record<string, Record<string, string>>,
	b?: Record<string, Record<string, string>>,
): Record<string, Record<string, string>> {
	if (!b) return a;
	const result: Record<string, Record<string, string>> = { ...a };
	for (const [locale, msgs] of Object.entries(b)) {
		result[locale] = { ...(result[locale] || {}), ...msgs };
	}
	return result;
}

/** Normalize a hooks value into an array (handles single entry or already-array). */
function normalizeHookEntries<T>(value: T | T[] | undefined): T[] {
	if (!value) return [];
	if (Array.isArray(value)) return value;
	return [value];
}

/** Concatenate global hook arrays (normalizes single-entry inputs). */
function mergeGlobalHooks(
	a: GlobalHooksState | undefined,
	b: GlobalHooksState | undefined,
): GlobalHooksState | undefined {
	if (!a && !b) return undefined;
	if (!a) return b;
	if (!b) return a;
	return {
		collections: [...normalizeHookEntries(a.collections), ...normalizeHookEntries(b.collections)],
		globals: [...normalizeHookEntries(a.globals), ...normalizeHookEntries(b.globals)],
	};
}

/**
 * Merge functions for known module keys.
 *
 * Each entry maps a key name to its merge function: `(existing, incoming) → merged`.
 * Keys not in this map use auto-detect merge (see {@link mergeGenericKey}):
 * - Object + Object → spread merge
 * - Array + Array → concatenate
 * - Otherwise → incoming wins
 *
 * Uses actual functions (not string strategies) so plugins can provide
 * arbitrary merge logic for their contributed keys.
 *
 * Common helpers are exported for reuse:
 * {@link mergeRecord}, {@link mergeConcat}, {@link lastWins}.
 *
 * @example
 * ```ts
 * // A plugin can register custom merge for its keys:
 * MERGE_FNS.set("auditRules", mergeConcat);
 * ```
 */
// ============================================================================
// Config bucket merge
// ============================================================================

/**
 * Per-config-key sub-property merge strategies.
 * Each config key (app, admin, auth, etc.) can declare how its sub-properties
 * should be merged when multiple modules contribute the same config key.
 *
 * Keys not listed here use lastWins (incoming replaces existing).
 * Config keys not in this map at all use lastWins on the whole object.
 */
const CONFIG_KEY_MERGE = new Map<string, Map<string, MergeFn>>([
	[
		"app",
		new Map<string, MergeFn>([
			["locale", lastWins],
			["access", lastWins],
			["hooks", (a, b) => mergeGlobalHooks(a, b)],
			["context", lastWins],
		]),
	],
	[
		"admin",
		new Map<string, MergeFn>([
			["sidebar", mergeDeepConcat],
			["dashboard", mergeDeepConcat],
			["branding", lastWins],
			["locale", lastWins],
		]),
	],
]);

/**
 * Merge a single config key using its declared sub-property merge strategies.
 * Falls back to lastWins for unknown sub-keys.
 */
function mergeConfigKey(
	configKey: string,
	existing: any,
	incoming: any,
): any {
	// Special case: auth is deep-merged as a whole via mergeAuthOptions
	if (configKey === "auth") {
		return mergeAuthOptions(existing ?? {}, incoming ?? {});
	}

	const subKeyMerges = CONFIG_KEY_MERGE.get(configKey);
	if (!subKeyMerges) {
		// No sub-key merge declared — lastWins on the whole config key
		return incoming;
	}

	// Sub-key-level merge
	const result = { ...(existing || {}) };
	for (const [key, value] of Object.entries(incoming || {})) {
		const subMerge = subKeyMerges.get(key);
		if (subMerge && result[key] !== undefined) {
			result[key] = subMerge(result[key], value);
		} else {
			result[key] = value;
		}
	}
	return result;
}

/**
 * Merge the `config` bucket from two module-like objects.
 * Each key in the config bucket (app, auth, admin, openapi, etc.)
 * is merged independently using its per-key merge strategy.
 */
function mergeConfigBucket(existing: any, incoming: any): any {
	const result = { ...(existing || {}) };
	for (const [key, value] of Object.entries(incoming || {})) {
		if (result[key] !== undefined) {
			result[key] = mergeConfigKey(key, result[key], value);
		} else {
			result[key] = value;
		}
	}
	return result;
}

// ============================================================================
// Module merge functions
// ============================================================================

const MERGE_FNS = new Map<string, MergeFn>([
	["collections", mergeRecord],
	["globals", mergeRecord],
	["jobs", mergeRecord],
	["routes", mergeRecord],
	["fields", mergeRecord],
	["services", mergeRecord],
	["messages", (a, b) => mergeMessages(a, b)],
	["migrations", mergeConcat],
	["seeds", mergeConcat],
	["config", mergeConfigBucket],
]);

/** Keys skipped during module merging (structural, not data). */
const STRUCTURAL_KEYS = new Set(["name", "modules", "plugin"]);

/**
 * Known infrastructure keys on RuntimeConfig that should NOT be forwarded
 * to `instance.state`. Only truly unknown extension keys pass through.
 *
 * @see RuntimeConfig in module-types.ts for the full list.
 */
const RUNTIME_CONSUMED_KEYS = new Set([
	"plugins",
	"app",
	"db",
	"secret",
	"storage",
	"email",
	"queue",
	"search",
	"realtime",
	"logger",
	"kv",
	"translations",
	"autoMigrate",
	"autoSeed",
	"cli",
]);

/**
 * Keys consumed by QuestpieConfig construction or definition metadata.
 * Everything NOT in this set flows to `instance.state` for plugins to read
 * (e.g., admin reads sidebar, dashboard, branding, blocks, views, components).
 *
 * Fully generic — any plugin's extension keys automatically flow through
 * without being listed anywhere.
 */
const CONFIG_CONSUMED_KEYS = new Set([
	// Module-mergeable keys (from MERGE_FNS → become QuestpieConfig properties)
	...MERGE_FNS.keys(),
	// Derived key (computed from messages + config, not from modules directly)
	"translations",
	// Definition-only keys (used directly in config construction, not merged)
	"emailTemplates",
	// Structural
	"name",
	"modules",
	// Codegen-only (never leaks to instance.state)
	"plugin",
]);

/** Create an empty merged state with correct initial values for known keys. */
function emptyMergedState(): Record<string, any> {
	return {
		collections: {},
		globals: {},
		jobs: {},
		routes: {},
		fields: {},
		services: {},
		migrations: [],
		seeds: [],
		messages: {},
		config: {},
		translations: undefined,
	};
}

/**
 * Merge a module (or module-like object) into accumulated state.
 *
 * For keys in {@link MERGE_FNS}, the declared merge function is used.
 * For unknown keys (plugin extensions), auto-detect merge is applied.
 * Structural keys ("name", "modules") are skipped.
 */
function mergeModuleIntoState(
	state: Record<string, any>,
	mod: Record<string, any>,
): Record<string, any> {
	const result: Record<string, any> = { ...state };

	for (const key of Object.keys(mod)) {
		if (STRUCTURAL_KEYS.has(key)) continue;

		const incoming = mod[key];
		if (incoming === undefined) continue;

		const mergeFn = MERGE_FNS.get(key);
		if (mergeFn) {
			result[key] = mergeFn(state[key], incoming);
		} else {
			mergeGenericKey(result, key, state[key], incoming);
		}
	}

	return result;
}

/**
 * Auto-detect merge for extension keys (not in {@link MERGE_FNS}):
 * - Array + Array → concatenate
 * - Object + Object → spread merge
 * - Otherwise → incoming wins
 */
function mergeGenericKey(
	result: Record<string, any>,
	key: string,
	existing: any,
	incoming: any,
): void {
	if (Array.isArray(incoming) && Array.isArray(existing)) {
		result[key] = [...existing, ...incoming];
	} else if (Array.isArray(incoming)) {
		result[key] = [...incoming];
	} else if (
		incoming != null &&
		typeof incoming === "object" &&
		!Array.isArray(incoming) &&
		typeof existing === "object" &&
		!Array.isArray(existing) &&
		existing != null
	) {
		result[key] = { ...existing, ...incoming };
	} else {
		result[key] = incoming;
	}
}

// ============================================================================
// Extension state extraction
// ============================================================================

/**
 * Extract extension state — all merged keys NOT consumed by QuestpieConfig.
 * Stored on `instance.state` for plugins to read (e.g., admin reads
 * sidebar, dashboard, branding, blocks, views, components).
 *
 * Fully generic — any plugin's extension keys automatically flow through
 * without being listed anywhere.
 *
 * @param merged - Fully merged state from all modules + user entities
 * @param configOverrides - Config-level overrides (legacy createApp only)
 */
function buildExtensionState(
	merged: Record<string, any>,
	configOverrides?: Record<string, unknown>,
): Record<string, unknown> {
	const extensionState: Record<string, unknown> = {};

	// Extract all non-config keys from merged state
	for (const [key, value] of Object.entries(merged)) {
		if (!CONFIG_CONSUMED_KEYS.has(key) && value !== undefined) {
			extensionState[key] = value;
		}
	}

	// Apply config-level overrides (legacy mode only)
	if (configOverrides) {
		for (const [key, value] of Object.entries(configOverrides)) {
			if (CONFIG_CONSUMED_KEYS.has(key) || value === undefined) continue;

			const existing = extensionState[key];

			// Array: append config-level to module-level
			if (Array.isArray(existing) && value != null) {
				extensionState[key] = Array.isArray(value)
					? [...existing, ...value]
					: [...existing, value];
			}
			// Otherwise: config-level overrides
			else {
				extensionState[key] = value;
			}
		}
	}

	// Auto-build any builder instances in extension state records.
	// This handles e.g. BlockBuilder → BlockDefinition without coupling
	// the core engine to specific builder types from other packages.
	// Duck-type check: if a value has a .build() method, call it.
	for (const [key, value] of Object.entries(extensionState)) {
		if (value && typeof value === "object" && !Array.isArray(value)) {
			const record = value as Record<string, unknown>;
			let rebuilt = false;
			const builtRecord: Record<string, unknown> = {};
			for (const [k, v] of Object.entries(record)) {
				if (v && typeof (v as any).build === "function") {
					builtRecord[k] = (v as any).build();
					rebuilt = true;
				} else {
					builtRecord[k] = v;
				}
			}
			if (rebuilt) {
				extensionState[key] = builtRecord;
			}
		}
	}

	return extensionState;
}

/**
 * Extract unknown extension keys from RuntimeConfig.
 * Filters out known infrastructure keys so only truly unknown
 * plugin-contributed keys (e.g. `channels`, `workflows`) flow through
 * to `instance.state`.
 */
function extractRuntimeExtensions(
	runtime: Record<string, unknown> | undefined,
): Record<string, unknown> | undefined {
	if (!runtime) return undefined;
	const extensions: Record<string, unknown> = {};
	let hasAny = false;
	for (const [key, value] of Object.entries(runtime)) {
		if (!RUNTIME_CONSUMED_KEYS.has(key) && value !== undefined) {
			extensions[key] = value;
			hasAny = true;
		}
	}
	return hasAny ? extensions : undefined;
}

