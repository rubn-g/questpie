// ============================================================================
// runtimeConfig() — cloud-aware runtime config with env auto-detection
// ============================================================================

/**
 * Define the runtime configuration for `questpie.config.ts`.
 * Contains only runtime infrastructure (db, adapters, secret) and plugin registrations.
 * No entity definitions — those come from the definition object generated by codegen.
 *
 * **Cloud-aware**: `app` and `db` are optional. When omitted they are resolved
 * automatically from environment variables:
 *
 * | Field     | QUESTPIE Cloud env        | Standard fallback       | Default              |
 * |-----------|---------------------------|-------------------------|----------------------|
 * | `app.url` | `QUESTPIE_APP_URL`        | `APP_URL`               | `http://localhost:3000` |
 * | `db.url`  | `QUESTPIE_DB`             | `DATABASE_URL`          | *(throws)*           |
 * | `secret`  | `QUESTPIE_SECRET`         | `BETTER_AUTH_SECRET`    | `undefined`          |
 * | `storage` | `QUESTPIE_STORAGE_*`      | —                       | local `./uploads`    |
 *
 * When `QUESTPIE_STORAGE_ENDPOINT`, `QUESTPIE_STORAGE_BUCKET`,
 * `QUESTPIE_STORAGE_ACCESS_KEY`, and `QUESTPIE_STORAGE_SECRET_KEY` are all set,
 * an S3-compatible storage driver is auto-configured (requires `@aws-sdk/client-s3`).
 *
 * @example Zero-config on QUESTPIE Cloud:
 * ```ts
 * export default runtimeConfig({
 *   plugins: [adminPlugin()],
 * });
 * ```
 *
 * @example Explicit overrides:
 * ```ts
 * export default runtimeConfig({
 *   plugins: [adminPlugin()],
 *   app: { url: process.env.APP_URL! },
 *   db: { url: process.env.DATABASE_URL! },
 *   secret: process.env.SECRET,
 *   email: { adapter: new ConsoleAdapter() },
 * });
 * ```
 *
 * @see RFC-MODULE-ARCHITECTURE §3.1 (Functions)
 */
export function runtimeConfig(input: RuntimeConfigInput): RuntimeConfig {
	return {
		...input,
		app: { url: resolveAppUrl(input.app?.url) },
		db: resolveDbConfig(input.db),
		secret: resolveSecret(input.secret as string | undefined),
		storage: resolveStorageConfig(input.storage as StorageConfig | undefined),
	};
}

// ============================================================================
// Module resolution — depth-first, left-to-right
// ============================================================================

/**
 * Flatten modules depth-first per RFC §13.7.
 * Dependencies are resolved before the module that depends on them.
 * Duplicate modules (by name) are deduplicated — last occurrence wins.
 */
function resolveModules(modules: ModuleDefinition[]): ModuleDefinition[] {
	const flat: ModuleDefinition[] = [];
	for (const mod of modules) {
		if (mod.modules && mod.modules.length > 0) {
			flat.push(...resolveModules(mod.modules));
		}
		flat.push(mod);
	}
	// Deduplicate by name — keep last occurrence (later wins)
	const seen = new Map<string, number>();
	for (let i = 0; i < flat.length; i++) {
		seen.set(flat[i].name, i);
	}
	return flat.filter((_, i) => {
		const mod = flat[i];
		return seen.get(mod.name) === i;
	});
}

