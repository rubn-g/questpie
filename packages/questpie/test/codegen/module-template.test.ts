function makeFile(
	key: string,
	opts: {
		varName?: string;
		importPath?: string;
		exportType?: "default" | "named" | "unknown";
		namedExportName?: string;
		source?: string;
		isBundle?: boolean;
	} = {},
): DiscoveredFile {
	return {
		absolutePath: `/root/${key}.ts`,
		key,
		varName: opts.varName ?? `_${key.replace(/[./]/g, "_")}`,
		importPath: opts.importPath ?? `../${key}`,
		exportType: opts.exportType ?? "default",
		namedExportName: opts.namedExportName,
		source: opts.source ?? `${key}.ts`,
		isBundle: opts.isBundle,
	};
}

function emptyResult(categoryNames: string[] = []): DiscoveryResult {
	const categories = new Map<string, Map<string, DiscoveredFile>>();
	for (const name of categoryNames) {
		categories.set(name, new Map());
	}
	return {
		categories,
		singles: new Map(),
		spreads: new Map(),
	};
}

/** Minimal category meta with just collections and jobs. */
function baseCategoryMeta(): Map<string, CategoryDeclaration> {
	return new Map<string, CategoryDeclaration>([
		[
			"collections",
			{
				dirs: ["collections"],
				prefix: "coll",
				emit: "record",
				registryKey: true,
			},
		],
		[
			"globals",
			{
				dirs: ["globals"],
				prefix: "glob",
				emit: "record",
				registryKey: true,
			},
		],
		[
			"jobs",
			{
				dirs: ["jobs"],
				prefix: "job",
				emit: "record",
				registryKey: true,
			},
		],
		[
			"routes",
			{
				dirs: ["routes", "functions"],
				prefix: "route",
				emit: "record",
				recursive: true,
				keySeparator: "/",
				registryKey: true,
				includeInAppState: true,
			},
		],
		[
			"migrations",
			{
				dirs: ["migrations"],
				prefix: "mig",
				emit: "array",
				typeEmit: "none",
			},
		],
		[
			"seeds",
			{
				dirs: ["seeds"],
				prefix: "seed",
				emit: "array",
				typeEmit: "none",
			},
		],
		[
			"messages",
			{
				dirs: ["messages"],
				prefix: "msg",
				emit: "record",
				typeEmit: "messages",
			},
		],
	]);
}

function cat(
	result: DiscoveryResult,
	name: string,
): Map<string, DiscoveredFile> {
	let map = result.categories.get(name);
	if (!map) {
		map = new Map();
		result.categories.set(name, map);
	}
	return map;
}

