/**
 * Factory Template Generation
 *
 * Generates `.generated/factories.ts` — typed `collection()` and `global()`
 * factory functions with plugin extensions, singleton factory functions
 * (branding, sidebar, locale, etc.), and module-driven type extraction
 * for autocomplete on proxies (views, components, field types).
 *
 * Extension methods are intercepted by a Proxy wrapper that delegates to
 * `builder.set()`. Core builder methods are passed through and their results
 * are re-wrapped to preserve the Proxy chain across immutable builder calls.
 *
 * @see RFC-CONTEXT-FIRST §6 (Extension System Without Monkey-Patching)
 */

import type {
	CallbackParamDefinition,
	ModuleRegistryConfig,
	RegistryExtension,
	ResolvedTarget,
	SingletonFactory,
} from "./types.js";

// ============================================================================
// Factory generation
// ============================================================================

export interface FactoryTemplateOptions {
	/** The resolved target with merged registries and callback params. */
	target: ResolvedTarget;
	/** Whether modules.ts exists (new architecture). */
	hasModules: boolean;
}

/**
 * Generate `.generated/factories.ts` content.
 * Returns null if the target declares no registries (no factories needed).
 */
export function generateFactoryTemplate(
	options: FactoryTemplateOptions,
): string | null {
	const { target, hasModules } = options;

	// Extract merged registries and callback params from the resolved target
	const collExtensions = new Map<string, RegistryExtension>();
	const globalExtensions = new Map<string, RegistryExtension>();
	const singletonFactories = new Map<string, SingletonFactory>();
	const allImports = new Map<string, Set<string>>(); // from → Set<name>

	for (const [name, ext] of Object.entries(
		target.registries.collectionExtensions,
	)) {
		collExtensions.set(name, ext);
		collectImports(ext, allImports);
	}

	for (const [name, ext] of Object.entries(
		target.registries.globalExtensions,
	)) {
		globalExtensions.set(name, ext);
		collectImports(ext, allImports);
	}

	for (const [name, factory] of Object.entries(
		target.registries.singletonFactories,
	)) {
		singletonFactories.set(name, factory);
		collectSingletonImports(factory, allImports);
	}

	// Module registries from the resolved target
	const moduleRegistries = new Map<string, ModuleRegistryConfig>();
	for (const [key, config] of Object.entries(
		target.registries.moduleRegistries,
	)) {
		moduleRegistries.set(key, config);
	}

	// Callback params from the resolved target
	const callbackParams = new Map<string, CallbackParamDefinition>();
	for (const [key, def] of Object.entries(target.callbackParams)) {
		callbackParams.set(key, def);
	}

	// If nothing declared, no factories file needed
	if (
		collExtensions.size === 0 &&
		globalExtensions.size === 0 &&
		singletonFactories.size === 0
	) {
		return null;
	}

	// Collect all configTypePlaceholders → category mappings
	const placeholderCategories = new Map<string, string>();
	for (const ext of [
		...collExtensions.values(),
		...globalExtensions.values(),
	]) {
		if (ext.configTypePlaceholders) {
			for (const [placeholder, category] of Object.entries(
				ext.configTypePlaceholders,
			)) {
				placeholderCategories.set(placeholder, category);
			}
		}
	}

	const lines: string[] = [];

	// Header
	lines.push("/* eslint-disable */");
	lines.push("// AUTO-GENERATED by questpie codegen — DO NOT EDIT");
	lines.push(
		"// Typed factory functions with plugin extensions. Regenerate with: questpie generate",
	);
	lines.push("");

	// Core imports
	lines.push("// ── Core Imports ───────────────────────────────────────────");
	lines.push(
		'import { CollectionBuilder, GlobalBuilder, type EmptyCollectionState, type EmptyGlobalState } from "questpie";',
	);
	lines.push("");

	// Module import (for type extraction AND runtime field merging)
	if (hasModules) {
		lines.push("// ── Module import (for type extraction + runtime fields) ──");
		lines.push('import _modulesArr from "../modules";');
		lines.push("");
	}

	// Plugin imports (only types needed for signatures)
	if (allImports.size > 0) {
		lines.push(
			"// ── Plugin Imports ─────────────────────────────────────────",
		);
		for (const [from, names] of allImports) {
			const nameList = [...names]
				.filter((n) => /^[A-Z]/.test(n)) // only types (PascalCase)
				.map((n) => `type ${n}`)
				.join(", ");
			const valueList = [...names]
				.filter((n) => /^[a-z]/.test(n)) // runtime values (camelCase)
				.join(", ");
			const parts = [nameList, valueList].filter(Boolean).join(", ");
			if (parts) {
				lines.push(`import { ${parts} } from "${from}";`);
			}
		}
		lines.push("");
	}

	// ── Module type extraction ──────────────────────────────
	if (hasModules) {
		lines.push(
			"// ════════════════════════════════════════════════════════════",
		);
		lines.push(
			"// Module type extraction — views, components, field types (all from modules)",
		);
		lines.push(
			"// ════════════════════════════════════════════════════════════",
		);
		lines.push("");

		lines.push("type _FlattenModules<T> = T extends readonly (infer M)[]");
		lines.push(
			"  ? M | (M extends { modules: infer Sub } ? _FlattenModules<Sub> : never)",
		);
		lines.push("  : never;");
		lines.push("type _AllModules = _FlattenModules<typeof _modulesArr>;");
		lines.push("");

		// Shared utility: union-to-intersection (used by both registry and field type extraction)
		lines.push(
			"// Module registry helpers — extracted from module registrations",
		);
		lines.push(
			"type _U2I<U> = (U extends any ? (x: U) => void : never) extends (x: infer I) => void ? I : never;",
		);

		// Extract keys helper (string union of registry entry names)
		lines.push(
			"type _ExtractKeys<K extends string> = _AllModules extends infer M ? M extends Record<K, Record<infer V extends string, any>> ? V : never : never;",
		);
		// Extract full registry record (intersected from all modules)
		lines.push(
			"type _ExtractRegistry<K extends string> = _U2I<_AllModules extends infer M ? M extends Record<K, infer R> ? R : never : never>;",
		);

		// Dynamic: generate type alias for each moduleRegistry
		// Also generate strict (no string fallback) versions for type registry augmentations
		// Also generate record types when recordPlaceholder is set
		for (const [key, config] of moduleRegistries) {
			const typeName = `_${key.charAt(0).toUpperCase() + key.slice(1)}Names`;
			lines.push(`type ${typeName} = _ExtractKeys<"${key}"> | (string & {});`);
			if (config.typeRegistry) {
				lines.push(`type ${typeName}_Strict = _ExtractKeys<"${key}">;`);
			}
			if (config.recordPlaceholder) {
				const recordTypeName = `_${key.charAt(0).toUpperCase() + key.slice(1)}Record`;
				lines.push(
					`type ${recordTypeName} = [_ExtractRegistry<"${key}">] extends [never] ? Record<string, any> : _ExtractRegistry<"${key}">;`,
				);
			}
		}
		lines.push("");

		// Extract field types from modules (all field types come from modules — no hardcoded builtins)
		lines.push(
			"// Field types — all field types come from modules (starter, admin, etc.)",
		);
		lines.push(
			"type _ModuleFieldTypes = _U2I<_AllModules extends infer M ? M extends { fields: infer F } ? F : never : never>;",
		);
		lines.push(
			"type _AllFieldTypes = [_ModuleFieldTypes] extends [never] ? Record<string, never> : _ModuleFieldTypes;",
		);
		lines.push("");
	} else {
		// No modules — use string fallback for all module registries
		lines.push("// No modules.ts — using string fallback for type extraction");
		for (const [key, config] of moduleRegistries) {
			const typeName = `_${key.charAt(0).toUpperCase() + key.slice(1)}Names`;
			lines.push(`type ${typeName} = string;`);
			if (config.typeRegistry) {
				lines.push(`type ${typeName}_Strict = never;`);
			}
			if (config.recordPlaceholder) {
				const recordTypeName = `_${key.charAt(0).toUpperCase() + key.slice(1)}Record`;
				lines.push(`type ${recordTypeName} = Record<string, any>;`);
			}
		}
		lines.push("type _AllFieldTypes = Record<string, never>;");
		lines.push("");
	}

	// ── Runtime field merging ────────────────────────────────
	if (hasModules) {
		lines.push(
			"// ── Runtime: extract fields from modules ────────────────────",
		);
		lines.push(
			"function _extractModuleFields(modules: readonly any[]): Record<string, any> {",
		);
		lines.push("\tconst merged: Record<string, any> = {};");
		lines.push("\tfor (const m of modules) {");
		lines.push("\t\tif (m.fields) Object.assign(merged, m.fields);");
		lines.push(
			"\t\tif (m.modules) Object.assign(merged, _extractModuleFields(m.modules));",
		);
		lines.push("\t}");
		lines.push("\treturn merged;");
		lines.push("}");
		lines.push("const _allRuntimeFields = _extractModuleFields(_modulesArr);");
		lines.push("");
	}

	// ── Type augmentations (declare module) ────────────────────
	lines.push("// ════════════════════════════════════════════════════════════");
	lines.push("// Type augmentations — generated from plugin registries");
	lines.push("// ════════════════════════════════════════════════════════════");
	lines.push("");

	const placeholderMap = buildPlaceholderMap(moduleRegistries);

	lines.push('declare module "questpie" {');

	if (collExtensions.size > 0) {
		lines.push("\tinterface CollectionBuilder<TState> {");
		for (const [name, ext] of collExtensions) {
			const paramName = ext.isCallback ? "configFn" : "config";
			let paramType = ext.configType ?? "any";
			paramType = resolvePlaceholders(paramType, hasModules, placeholderMap);
			lines.push(
				`\t\t${name}(${paramName}: ${paramType}): CollectionBuilder<TState>;`,
			);
		}
		lines.push("\t}");
	}

	if (globalExtensions.size > 0) {
		lines.push("\tinterface GlobalBuilder<TState> {");
		for (const [name, ext] of globalExtensions) {
			const paramName = ext.isCallback ? "configFn" : "config";
			let paramType = ext.configType ?? "any";
			paramType = resolvePlaceholders(paramType, hasModules, placeholderMap);
			lines.push(
				`\t\t${name}(${paramName}: ${paramType}): GlobalBuilder<TState>;`,
			);
		}
		lines.push("\t}");
	}

	// Registry entries from module registries
	const registryEntries = [...moduleRegistries.entries()].filter(
		([_, c]) => c.registryKey,
	);
	if (registryEntries.length > 0) {
		lines.push("\tinterface Registry {");
		for (const [key, config] of registryEntries) {
			const typeName = `_${key.charAt(0).toUpperCase() + key.slice(1)}Names`;
			lines.push(`\t\t${config.registryKey}: Record<${typeName}, unknown>;`);
		}
		lines.push("\t}");
	}

	// FieldTypeRegistry augmentation — always in the "questpie" module
	// Uses keyof _AllFieldTypes so it narrows to exact field type literals
	lines.push(
		"\tinterface FieldTypeRegistry extends Record<keyof _AllFieldTypes, {}> {}",
	);

	// Plugin type registries that target "questpie" module
	for (const [key, config] of moduleRegistries) {
		if (config.typeRegistry?.module === "questpie") {
			const strictName = `_${key.charAt(0).toUpperCase() + key.slice(1)}Names_Strict`;
			lines.push(
				`\tinterface ${config.typeRegistry.interface} extends Record<${strictName}, {}> {}`,
			);
		}
	}

	lines.push("}");
	lines.push("");

	// Plugin type registries that target other modules (e.g., "@questpie/admin/server")
	// Group by module to minimize declare module blocks
	const externalTypeRegistries = new Map<
		string,
		Array<{ key: string; config: ModuleRegistryConfig }>
	>();
	for (const [key, config] of moduleRegistries) {
		if (config.typeRegistry && config.typeRegistry.module !== "questpie") {
			let entries = externalTypeRegistries.get(config.typeRegistry.module);
			if (!entries) {
				entries = [];
				externalTypeRegistries.set(config.typeRegistry.module, entries);
			}
			entries.push({ key, config });
		}
	}

	for (const [moduleName, entries] of externalTypeRegistries) {
		lines.push(`declare module "${moduleName}" {`);
		for (const { key, config } of entries) {
			const strictName = `_${key.charAt(0).toUpperCase() + key.slice(1)}Names_Strict`;
			lines.push(
				`\tinterface ${config.typeRegistry?.interface} extends Record<${strictName}, {}> {}`,
			);
		}
		lines.push("}");
		lines.push("");
	}

	// ── Extension registries + Proxy wrappers ─────────────────
	lines.push("// ════════════════════════════════════════════════════════════");
	lines.push("// Extension registries + Proxy wrappers");
	lines.push("// ════════════════════════════════════════════════════════════");
	lines.push("");

	if (collExtensions.size > 0) {
		emitExtensionRegistry(lines, "_collExt", collExtensions, callbackParams);
		lines.push("");
		emitProxyWrapper(lines, "_wrapColl", "_collExt", "CollectionBuilder");
		lines.push("");
	}

	if (globalExtensions.size > 0) {
		emitExtensionRegistry(lines, "_globExt", globalExtensions, callbackParams);
		lines.push("");
		emitProxyWrapper(lines, "_wrapGlob", "_globExt", "GlobalBuilder");
		lines.push("");
	}

	// ── Factory functions ─────────────────────────────────────
	lines.push("// ════════════════════════════════════════════════════════════");
	lines.push("// Factory functions");
	lines.push("// ════════════════════════════════════════════════════════════");
	lines.push("");

	// collection()
	if (collExtensions.size > 0) {
		lines.push("/**");
		lines.push(" * Create a typed collection builder with plugin extensions.");
		lines.push(" *");
		lines.push(" * @example");
		lines.push(" * ```ts");
		lines.push(' * import { collection } from "#questpie";');
		lines.push(" *");
		lines.push(' * export default collection("posts")');
		lines.push(
			" *   .fields(({ f }) => ({ title: f.text({ required: true }) }))",
		);
		lines.push(' *   .admin(({ c }) => ({ icon: c.icon("ph:article") }))');
		lines.push(" *   .list(({ v, f }) => v.table({ columns: [f.title] }))");
		lines.push(" * ```");
		lines.push(" */");
		lines.push(
			"export function collection<TName extends string>(name: TName): CollectionBuilder<EmptyCollectionState<TName, undefined, _AllFieldTypes>> {",
		);
		lines.push("\treturn _wrapColl(new CollectionBuilder({");
		lines.push("\t\tname: name as string,");
		lines.push("\t\tfields: {},");
		lines.push("\t\tlocalized: [],");
		lines.push("\t\tvirtuals: undefined,");
		lines.push("\t\trelations: {},");
		lines.push("\t\tindexes: {},");
		lines.push("\t\ttitle: undefined,");
		lines.push("\t\toptions: {},");
		lines.push("\t\thooks: {},");
		lines.push("\t\taccess: {},");
		lines.push("\t\tsearchable: undefined,");
		lines.push("\t\tvalidation: undefined,");
		lines.push("\t\toutput: undefined,");
		lines.push("\t\tupload: undefined,");
		lines.push("\t\tfieldDefinitions: {},");
		if (hasModules) {
			lines.push(
				'\t\t"~questpieApp": { state: { fields: _allRuntimeFields } },',
			);
		} else {
			lines.push('\t\t"~questpieApp": undefined,');
		}
		lines.push("\t})) as any;");
		lines.push("}");
		lines.push("");
	}

	// global()
	if (globalExtensions.size > 0) {
		lines.push("/**");
		lines.push(" * Create a typed global builder with plugin extensions.");
		lines.push(" */");
		lines.push(
			"export function global<TName extends string>(name: TName): GlobalBuilder<EmptyGlobalState<TName, undefined, _AllFieldTypes>> {",
		);
		lines.push("\treturn _wrapGlob(new GlobalBuilder({");
		lines.push("\t\tname: name as string,");
		lines.push("\t\tfields: {},");
		lines.push("\t\tlocalized: [],");
		lines.push("\t\tvirtuals: {},");
		lines.push("\t\trelations: {},");
		lines.push("\t\toptions: {},");
		lines.push("\t\thooks: {},");
		lines.push("\t\taccess: {},");
		lines.push("\t\tfieldDefinitions: {},");
		if (hasModules) {
			lines.push(
				'\t\t"~questpieApp": { state: { fields: _allRuntimeFields } },',
			);
		} else {
			lines.push('\t\t"~questpieApp": undefined,');
		}
		lines.push("\t})) as any;");
		lines.push("}");
		lines.push("");
	}

	// ── Singleton factory functions ────────────────────────────
	if (singletonFactories.size > 0) {
		lines.push(
			"// ════════════════════════════════════════════════════════════",
		);
		lines.push("// Singleton factory functions");
		lines.push(
			"// ════════════════════════════════════════════════════════════",
		);
		lines.push("");

		for (const [name, factory] of singletonFactories) {
			emitSingletonFactory(lines, name, factory);
			lines.push("");
		}
	}

	return lines.join("\n");
}

// ============================================================================
// Singleton factory emission
// ============================================================================

/**
 * Emit a singleton factory function.
 * Non-callback: simple generic identity with type constraint.
 * Callback: overloaded identity accepting both plain config and callback.
 */
function emitSingletonFactory(
	lines: string[],
	name: string,
	factory: SingletonFactory,
): void {
	const { configType, isCallback } = factory;

	if (isCallback) {
		// Overloaded — accepts plain config or callback function
		lines.push(
			`/** Typed factory for ${name} config. Accepts plain config or callback. */`,
		);
		lines.push(
			`export function ${name}<T extends ${configType}>(config: T): T;`,
		);
		lines.push(
			`export function ${name}<T extends (...args: any[]) => ${configType}>(cb: T): T;`,
		);
		lines.push(`export function ${name}(v: any): any { return v; }`);
	} else {
		// Simple identity with type constraint
		lines.push(`/** Typed factory for ${name} config. */`);
		lines.push(
			`export function ${name}<T extends ${configType}>(config: T): T { return config; }`,
		);
	}
}

