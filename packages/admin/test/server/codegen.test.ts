function makeFile(
	key: string,
	opts: {
		varName?: string;
		importPath?: string;
		exportType?: "default" | "named" | "unknown";
		namedExportName?: string;
		source?: string;
	} = {},
): DiscoveredFile {
	return {
		absolutePath: `/root/${key}.ts`,
		key,
		varName: opts.varName ?? `_${key.replace(/[./]/g, "_")}`,
		importPath: opts.importPath ?? `../${key}`,
		exportType: opts.exportType ?? "default",
		namedExportName: opts.namedExportName,
		source: opts.source ?? `${key}.ts`,
	};
}

function emptyDiscovery(): DiscoveryResult {
	return {
		categories: new Map(),
		auth: null,
		singles: new Map(),
		spreads: new Map(),
	};
}

function makeTarget(overrides: Partial<ResolvedTarget> = {}): ResolvedTarget {
	return {
		id: "admin-client",
		root: "../admin",
		outDir: ".generated",
		outputFile: "client.ts",
		categories: {},
		discover: {},
		registries: {
			collectionExtensions: {},
			globalExtensions: {},
			singletonFactories: {},
		},
		callbackParams: {},
		transforms: [],
		...overrides,
	};
}

function makeCtx(
	discovered: DiscoveryResult,
	target?: Partial<ResolvedTarget>,
): CodegenTargetGenerateContext {
	return {
		target: makeTarget(target),
		discovered,
		extraImports: [],
		extraTypeDeclarations: [],
		extraRuntimeCode: [],
		extraEntities: new Map(),
	};
}

function makeFakeResult(
	targetId: string,
	categories: Record<string, string[]>,
): CodegenResult {
	const catMap = new Map<string, Map<string, DiscoveredFile>>();
	for (const [catName, keys] of Object.entries(categories)) {
		const fileMap = new Map<string, DiscoveredFile>();
		for (const key of keys) {
			fileMap.set(
				key,
				makeFile(key, {
					varName: `_${catName}_${key}`,
					importPath: `../${catName}/${key}`,
					exportType: "default",
				}),
			);
		}
		catMap.set(catName, fileMap);
	}

	return {
		targetId,
		code: "// fake",
		outputPath: `/fake/.generated/${targetId}.ts`,
		discovered: {
			categories: catMap,
			auth: null,
			singles: new Map(),
			spreads: new Map(),
		},
	};
}

