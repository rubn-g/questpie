// ─────────────────────────────────────────────────────────────────────────────
// Collections — record emission with type interface
// ─────────────────────────────────────────────────────────────────────────────

describe("generateModuleTemplate — collections", () => {
	const result = emptyResult(["collections"]);
	const colls = cat(result, "collections");
	colls.set(
		"posts",
		makeFile("posts", {
			varName: "_coll_posts",
			importPath: "../collections/posts",
		}),
	);
	colls.set(
		"comments",
		makeFile("comments", {
			varName: "_coll_comments",
			importPath: "../collections/comments",
		}),
	);

	const output = generateModuleTemplate({
		moduleName: "questpie-blog",
		discovered: result,
		categoryMeta: baseCategoryMeta(),
	});

	it("emits imports for each collection", () => {
		expect(output).toContain(
			'import _coll_comments from "../collections/comments";',
		);
		expect(output).toContain('import _coll_posts from "../collections/posts";');
	});

	it("emits type interface for collections", () => {
		expect(output).toContain("export interface BlogCollections {");
		expect(output).toContain("comments: typeof _coll_comments;");
		expect(output).toContain("posts: typeof _coll_posts;");
	});

	it("emits record in module object with type assertion", () => {
		expect(output).toContain("collections: {");
		expect(output).toContain("comments: _coll_comments,");
		expect(output).toContain("posts: _coll_posts,");
		expect(output).toContain("} as BlogCollections,");
	});
});

