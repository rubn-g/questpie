describe("generateAdminClientTemplate", () => {
	it("emits empty object when no modules or user files discovered", () => {
		const ctx = makeCtx(emptyDiscovery());
		const output = generateAdminClientTemplate(ctx);

		expect(output.code).toContain("const admin = {};");
		expect(output.code).toContain("export default admin;");
		// Should NOT reference coreAdminModule
		expect(output.code).not.toContain("coreAdminModule");
	});

	it("re-exports module directly when no user files discovered", () => {
		const disc = emptyDiscovery();
		disc.singles.set(
			"modules",
			makeFile("modules", {
				varName: "_mod",
				importPath: "../modules",
				exportType: "default",
			}),
		);

		const ctx = makeCtx(disc);
		const output = generateAdminClientTemplate(ctx);

		expect(output.code).toContain('import _mod from "../modules";');
		expect(output.code).toContain("const admin = _mod;");
		expect(output.code).toContain("export default admin;");
	});

	it("emits auto-generated header comment", () => {
		const ctx = makeCtx(emptyDiscovery());
		const output = generateAdminClientTemplate(ctx);

		expect(output.code).toContain("Auto-generated by questpie codegen");
		expect(output.code).toContain("do not edit");
	});

	it("imports and merges discovered blocks with module", () => {
		const disc = emptyDiscovery();

		// Module
		disc.singles.set(
			"modules",
			makeFile("modules", {
				varName: "_mod",
				importPath: "../modules",
				exportType: "default",
			}),
		);

		// User blocks
		const blocksMap = new Map<string, DiscoveredFile>();
		blocksMap.set(
			"hero",
			makeFile("hero", {
				varName: "_block_hero",
				importPath: "../blocks/hero",
				exportType: "default",
			}),
		);
		blocksMap.set(
			"cta",
			makeFile("cta", {
				varName: "_block_cta",
				importPath: "../blocks/cta",
				exportType: "default",
			}),
		);
		disc.categories.set("blocks", blocksMap);

		const ctx = makeCtx(disc, {
			categories: {
				blocks: {
					dirs: ["blocks"],
					prefix: "block",
					keyFromSource: "basename",
					registryKey: false,
					includeInAppState: false,
					extractFromModules: false,
				},
			},
		});
		const output = generateAdminClientTemplate(ctx);

		// Imports
		expect(output.code).toContain('import _mod from "../modules";');
		expect(output.code).toContain('import _block_cta from "../blocks/cta";');
		expect(output.code).toContain('import _block_hero from "../blocks/hero";');
		// Merged object with module spread
		expect(output.code).toContain("blocks: { ..._mod.blocks,");
		expect(output.code).toContain('"cta": _block_cta');
		expect(output.code).toContain('"hero": _block_hero');
	});

	it("keys discovered blocks by source basename", () => {
		const disc = emptyDiscovery();

		const blocksMap = new Map<string, DiscoveredFile>();
		blocksMap.set(
			"imageText",
			makeFile("imageText", {
				varName: "_block_imageText",
				importPath: "../blocks/image-text",
				source: "blocks/image-text.tsx",
			}),
		);
		disc.categories.set("blocks", blocksMap);

		const ctx = makeCtx(disc, {
			categories: {
				blocks: {
					dirs: ["blocks"],
					prefix: "block",
					keyFromSource: "basename",
					registryKey: false,
					includeInAppState: false,
					extractFromModules: false,
				},
			},
		});
		const output = generateAdminClientTemplate(ctx);

		expect(output.code).toContain(
			'blocks: { "image-text": _block_imageText },',
		);
		expect(output.code).not.toContain('"imageText": _block_imageText');
	});

	it("emits blocks without module when no modules discovered", () => {
		const disc = emptyDiscovery();

		const blocksMap = new Map<string, DiscoveredFile>();
		blocksMap.set(
			"hero",
			makeFile("hero", {
				varName: "_block_hero",
				importPath: "../blocks/hero",
				exportType: "default",
			}),
		);
		disc.categories.set("blocks", blocksMap);

		const ctx = makeCtx(disc, {
			categories: {
				blocks: {
					dirs: ["blocks"],
					prefix: "block",
					keyFromSource: "basename",
					registryKey: false,
					includeInAppState: false,
					extractFromModules: false,
				},
			},
		});
		const output = generateAdminClientTemplate(ctx);

		// No module import
		expect(output.code).not.toContain("_mod");
		// User blocks only
		expect(output.code).toContain('blocks: { "hero": _block_hero },');
	});

	it("merges discovered views using keyFromProperty", () => {
		const disc = emptyDiscovery();

		disc.singles.set(
			"modules",
			makeFile("modules", {
				varName: "_mod",
				importPath: "../modules",
				exportType: "default",
			}),
		);

		const viewsMap = new Map<string, DiscoveredFile>();
		viewsMap.set(
			"kanban",
			makeFile("kanban", {
				varName: "_view_kanban",
				importPath: "../views/kanban",
				exportType: "default",
			}),
		);
		disc.categories.set("views", viewsMap);

		const ctx = makeCtx(disc, {
			categories: {
				views: {
					dirs: ["views"],
					prefix: "view",
					registryKey: false,
					includeInAppState: false,
					extractFromModules: false,
					keyFromProperty: "name",
				},
			},
		});
		const output = generateAdminClientTemplate(ctx);

		// Should emit runtime key using keyFromProperty
		expect(output.code).toContain("views: { ..._mod.views,");
		expect(output.code).toContain("[_view_kanban.name]: _view_kanban");
	});

	it("merges discovered singles into state keys", () => {
		const disc = emptyDiscovery();
		disc.singles.set(
			"locale",
			makeFile("locale", {
				varName: "_locale",
				importPath: "../locale",
				exportType: "default",
			}),
		);
		disc.singles.set(
			"defaults",
			makeFile("defaults", {
				varName: "_defaults_file",
				importPath: "../defaults",
				exportType: "default",
			}),
		);

		const ctx = makeCtx(disc);
		const output = generateAdminClientTemplate(ctx);

		expect(output.code).toContain("locale: _locale,");
		expect(output.code).toContain("defaults: _defaults_file,");
	});

	it("handles named exports correctly", () => {
		const disc = emptyDiscovery();
		const blocksMap = new Map<string, DiscoveredFile>();
		blocksMap.set(
			"hero",
			makeFile("hero", {
				varName: "_block_hero",
				importPath: "../blocks/hero",
				exportType: "named",
				namedExportName: "heroBlock",
			}),
		);
		disc.categories.set("blocks", blocksMap);

		const ctx = makeCtx(disc, {
			categories: {
				blocks: {
					dirs: ["blocks"],
					prefix: "block",
					keyFromSource: "basename",
					registryKey: false,
					includeInAppState: false,
					extractFromModules: false,
				},
			},
		});
		const output = generateAdminClientTemplate(ctx);

		expect(output.code).toContain(
			'import { heroBlock as _block_hero } from "../blocks/hero";',
		);
	});

	it("includes extra runtime code from transforms", () => {
		const disc = emptyDiscovery();
		// Need at least one user file to trigger the object
		disc.singles.set(
			"locale",
			makeFile("locale", {
				varName: "_locale",
				importPath: "../locale",
				exportType: "default",
			}),
		);

		const ctx = makeCtx(disc);
		ctx.extraRuntimeCode.push('customProp: "hello",');

		const output = generateAdminClientTemplate(ctx);

		expect(output.code).toContain('customProp: "hello",');
	});

	it("includes extra type declarations from transforms", () => {
		const ctx = makeCtx(emptyDiscovery());
		ctx.extraTypeDeclarations.push("export type CustomType = string;");

		const output = generateAdminClientTemplate(ctx);

		expect(output.code).toContain("export type CustomType = string;");
	});

	it("merges multiple categories together with module", () => {
		const disc = emptyDiscovery();

		disc.singles.set(
			"modules",
			makeFile("modules", {
				varName: "_mod",
				importPath: "../modules",
				exportType: "default",
			}),
		);

		const blocksMap = new Map<string, DiscoveredFile>();
		blocksMap.set(
			"hero",
			makeFile("hero", {
				varName: "_block_hero",
				importPath: "../blocks/hero",
			}),
		);
		disc.categories.set("blocks", blocksMap);

		const fieldsMap = new Map<string, DiscoveredFile>();
		fieldsMap.set(
			"color",
			makeFile("color", {
				varName: "_fld_color",
				importPath: "../fields/color",
			}),
		);
		disc.categories.set("fields", fieldsMap);

		const pagesMap = new Map<string, DiscoveredFile>();
		pagesMap.set(
			"analytics",
			makeFile("analytics", {
				varName: "_pg_analytics",
				importPath: "../pages/analytics",
			}),
		);
		disc.categories.set("pages", pagesMap);

		const ctx = makeCtx(disc, {
			categories: {
				blocks: {
					dirs: ["blocks"],
					prefix: "block",
					keyFromSource: "basename",
					registryKey: false,
					includeInAppState: false,
					extractFromModules: false,
				},
				fields: {
					dirs: ["fields"],
					prefix: "fld",
					registryKey: false,
					includeInAppState: false,
					extractFromModules: false,
				},
				pages: {
					dirs: ["pages"],
					prefix: "pg",
					registryKey: false,
					includeInAppState: false,
					extractFromModules: false,
				},
			},
		});
		const output = generateAdminClientTemplate(ctx);

		expect(output.code).toContain("blocks: { ..._mod.blocks,");
		expect(output.code).toContain("fields: { ..._mod.fields,");
		expect(output.code).toContain("pages: { ..._mod.pages,");
		expect(output.code).toContain('"hero": _block_hero');
		expect(output.code).toContain('"color": _fld_color');
		expect(output.code).toContain('"analytics": _pg_analytics');
	});

	it("spreads module categories even when no user files exist for that category", () => {
		const disc = emptyDiscovery();

		disc.singles.set(
			"modules",
			makeFile("modules", {
				varName: "_mod",
				importPath: "../modules",
				exportType: "default",
			}),
		);

		// Only blocks from user, but fields/views/etc. declared in target
		const blocksMap = new Map<string, DiscoveredFile>();
		blocksMap.set(
			"hero",
			makeFile("hero", {
				varName: "_block_hero",
				importPath: "../blocks/hero",
			}),
		);
		disc.categories.set("blocks", blocksMap);

		const ctx = makeCtx(disc, {
			categories: {
				blocks: {
					dirs: ["blocks"],
					prefix: "block",
					keyFromSource: "basename",
					registryKey: false,
					includeInAppState: false,
					extractFromModules: false,
				},
				fields: {
					dirs: ["fields"],
					prefix: "fld",
					registryKey: false,
					includeInAppState: false,
					extractFromModules: false,
				},
				views: {
					dirs: ["views"],
					prefix: "view",
					registryKey: false,
					includeInAppState: false,
					extractFromModules: false,
					keyFromProperty: "name",
				},
			},
		});
		const output = generateAdminClientTemplate(ctx);

		// Module-only categories should be spread
		expect(output.code).toContain("fields: { ..._mod.fields },");
		expect(output.code).toContain("views: { ..._mod.views },");
		// User category should merge
		expect(output.code).toContain("blocks: { ..._mod.blocks,");
	});
});

